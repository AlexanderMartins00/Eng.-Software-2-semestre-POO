    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        // TODO add your handling code here:
        
        try {
            
            
            int matricula = (Integer.parseInt(jTextFieldMatricula.getText()));
            String nome = (jTextFieldNome.getText());
            int dependentes = (Integer.parseInt(jTextFieldNumeroDependentes.getText()));
            float salarioBase = (Integer.parseInt(jTextFieldSalarioBase.getText()));
            int gratificacao = (Integer.parseInt(jTextFieldGratificacao.getText()));
            
            colaboradores = new Funcionarios(matricula,nome,dependentes,salarioBase,gratificacao);
            
            colaboradores.exibirContracheque();
            jTextAreaSaida.setText(colaboradores.exibirContracheque());
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro);
        }
    }//GEN-LAST:event_jButtonCalcularActionPerformed

